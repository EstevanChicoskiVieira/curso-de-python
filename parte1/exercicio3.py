n = input('Digite algo: ')

print('\n\n')
print('é um valor alfabético: ', n.isalpha())
print('é um valor numérico: ', n.isnumeric())
print('é um valor alphanumérico: ', n.isalnum())
print('é um valor em maiusculo: ', n.isupper())
print('é um valor em mínusculo: ', n.islower())

